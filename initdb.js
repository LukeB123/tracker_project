async function initData() {
  const trackersInsert = db.prepare(`
        INSERT INTO projects VALUES (
            null,
            @slug,
            @title,
            @delivery_manager,
            @delivery_manager_id,
            @project_manager,
            @project_manager_id,
            @scrum_master,
            @scrum_master_id,
            @delivery_stream,
            @value_stream,
            @project_type,
            @last_updated,
            @line_of_business,
            @task
        )
    `);

  for (const project of projects) {
    trackersInsert.run(project);
  }

  const resourcesInsert = db.prepare(`
  INSERT INTO resources VALUES (
      null,
      @name,
      @slug,
      @email,
      @grade,
      @role_id,
      @role,
      @team,
      @is_delivery_manager,
      @is_project_manager,
      @is_scrum_master
  )
`);

  for (const resource of resources) {
    resourcesInsert.run(resource);
  }

  const rolesInsert = db.prepare(`
    INSERT INTO roles VALUES (
        null,
        @role
    )
  `);

  for (const role of roles) {
    rolesInsert.run(role);
  }

  const timeEntryInsert = db.prepare(`
  INSERT INTO time_entries VALUES (
      null,
      @project_id,
      @project_slug,
      @project_title,
      @resource_id,
      @resource_name,
      @resource_slug,
      @role_id,
      @role,
      @rate_grade,
      @week_commencing,
      @work_days
  )
`);

  for (const time_entry of hours) {
    timeEntryInsert.run(time_entry);
  }

  const dateEntryInsert = db.prepare(`
  INSERT INTO dates VALUES (
      @date,
      @week_commencing,
      @week_index,
      @month_index,
      @month_year_string,
      @year,
      @work_day
  )
`);

  for (const date of dates) {
    dateEntryInsert.run(date);
  }
}

